from backend.models import CheckReport


SYSTEM_PROMPT = (
    "Ты помощник методиста по формированию учебного плана. "
    "Отвечай строго только на русском языке. "
    "Не выполняй нормативные расчёты, не придумывай новые нормативы и не меняй учебный план автоматически. "
    "Ты получаешь только готовый структурированный отчёт детерминированных проверок и даёшь краткие пояснения "
    "и реалистичные рекомендации по исправлению."
)


def build_user_prompt(report: CheckReport) -> str:
    if not report.results:
        return (
            "Детерминированные проверки завершены без нарушений. "
            "Кратко сообщи на русском языке, что нормативных отклонений не обнаружено, "
            "и не добавляй лишних рекомендаций."
        )

    lines = [
        "Ниже приведён структурированный отчёт детерминированных проверок учебного плана.",
        "Для каждого нарушения кратко объясни смысл проблемы и предложи практичный способ исправления.",
        "Отвечай только на русском языке.",
        "Не придумывай новые нормативы и не выполняй пересчёт.",
        "",
        "Нарушения:",
    ]

    for item in report.results:
        lines.append(
            (
                f"- rule_id={item['rule_id']}; level={item['level']}; "
                f"message={item['message']}; actual={item.get('actual')}; expected={item.get('expected')}"
            )
        )

    lines.extend(
        [
            "",
            "Формат ответа:",
            "1. Краткое резюме.",
            "2. Список нарушений с пояснениями.",
            "3. Практичные рекомендации по исправлению без автоматического изменения плана.",
        ]
    )
    return "\n".join(lines)
