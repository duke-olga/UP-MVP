from backend.models import CheckReport


SYSTEM_PROMPT = (
    "Ты помощник методиста по формированию учебного плана. "
    "Ты не выполняешь нормативные расчеты и не меняешь план. "
    "Ты получаешь готовый структурированный отчет проверок и кратко объясняешь нарушения, "
    "а также предлагаешь реалистичные шаги исправления."
)


def build_user_prompt(report: CheckReport) -> str:
    if not report.results:
        return (
            "Детерминированные проверки завершены без нарушений. "
            "Кратко сообщи, что нормативных отклонений не обнаружено."
        )

    lines = [
        "Ниже приведен структурированный отчет детерминированных проверок учебного плана.",
        "Для каждого нарушения кратко объясни смысл проблемы и предложи практичный способ исправления.",
        "Не придумывай новые нормативы и не выполняй пересчет.",
        "",
        "Нарушения:",
    ]

    for item in report.results:
        lines.append(
            (
                f"- rule_id={item['rule_id']}; level={item['level']}; "
                f"message={item['message']}; actual={item.get('actual')}; expected={item.get('expected')}"
            )
        )

    lines.extend(
        [
            "",
            "Формат ответа:",
            "1. Краткое резюме.",
            "2. Список нарушений с пояснениями.",
            "3. Практичные рекомендации по исправлению без автоматического изменения плана.",
        ]
    )
    return "\n".join(lines)
